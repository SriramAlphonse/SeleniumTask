package Task1;

public class Inheritance1 {
	
	public void Apple1() {
		
		System.out.println("Price of Apple1 type is 150");
	}
	
    public void Apple2() {
		
		System.out.println("Price of Apple2 type is 200");
	}

	public static void main(String[] args) {
		Inheritance1 i = new Inheritance1();
		i.Apple1();
		i.Apple2();

	}

}
